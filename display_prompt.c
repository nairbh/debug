#include "shell.h"

/**
 * display_prompt - Prints a shell prompt
 *
 * Description: This function prints a shell prompt to standard output.
 *              The prompt is represented by the dollar sign ($) followed
 *              by a space.
 *
 * Return: This function does not return a value.
 */
void display_prompt(void)
{
	printf("$ ");
}
